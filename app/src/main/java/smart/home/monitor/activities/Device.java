package smart.home.monitor.activities;


public class Device {
    private Integer code;
    private String name;

    public Device() {
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
